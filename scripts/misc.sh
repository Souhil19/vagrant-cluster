set -e 
_error(){
	printf ${RED}"ERROR:${RESET} $@" >&2
}
_info(){
	printf ${YELLOW}"INFO:${RESET} $@"
}
_done(){
	echo ${GREEN}" DONE"${RESET}
}
_ok(){
    _pos=$(( $(tput cols) - 5 ))
	echo ${GREEN}" [OK]"${RESET}
}
_war(){
    _pos=$(( $(tput cols) - 5 ))
	echo ${YELLOW}" [!]"${RESET}
}
_underline() {
  echo "$(printf '\033[4m')$@$(printf '\033[24m')"
}

#_code() {
  #echo "\`$(printf '\033[38;5;247m')$@${RESET}\`"
#}

_loading() {
  echo -n "$(printf '\033[38;5')$@${RESET}"
}

h_line(){
    cls=$(tput cols) && \
    for i in {0..$cls};do
        echo -n "$1"
    done
    echo ""
}

to_lower(){
    echo ${1} | tr '[a-z]' '[A-Z]'
}

to_upper(){
    echo ${1} | tr '[A-Z]' '[a-z]'
}

command_exists(){
    command -v "$@" >/dev/null 2>&1
}
user_exists(){
    grep -x "$@" /etc/passwd >/dev/null 2>&1
}
setup_color(){
	# Only use color if connected to a terminal
	if [ -t 1 ]; then
		RED=$(printf '\033[0;31m')
		GREEN=$(printf '\033[0;32m')
		YELLOW=$(printf '\033[0;33m')
		BLUE=$(printf '\033[0;34m')
		PURPLE=$(printf '\033[0;35m')
		CYAN=$(printf '\033[0;36m')
		BOLD=$(printf '\033[1m')
		RESET=$(printf '\033[0m')
	else
		RED=''
		GREEN=''
		YELLOW=''
		BLUE=''
		PURPLE=''
		CYAN=''
		BOLD=''
		RESET=''
	fi
}
